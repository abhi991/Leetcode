//Function to find the data of nth node from the end of a linked list.
int getSize(Node* head){
    int ans=0;
    while(head!=NULL){
        ans++;
        head = head->next;
    }
    return ans;
}
int getNthFromLast(Node *head, int n){
    int sz = getSize(head);
    if(n > sz) return -1;
    n = sz-n;
    int cnt=0;
    while(cnt < n){
        cnt++;
        head = head->next;
    }
    return head->data;
}

